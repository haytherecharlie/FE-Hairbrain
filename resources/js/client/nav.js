var Nav = (function() {

//----------------------------------------------------------------

						 // CACHE

//---------------------------------------------------------------/

/*******************************************
 * Global Variables
*******************************************/
var navMenu     = $('.navmenu'),
	menuItems   = $('.menuitems'),
	menuOverlay = $('.menuoverlay');

//----------------------------------------------------------------

						 // TEMPLATES

//---------------------------------------------------------------/

//----------------------------------------------------------------

						 // LISTENERS

//---------------------------------------------------------------/
navMenu.click(function() {
	if(menuItems.hasClass('open'))
		closeMenu();
	else
		openMenu();
});

//----------------------------------------------------------------

						 // VIEWS

//---------------------------------------------------------------/
function openMenu() {
	menuItems.animate({
		bottom: '-=' + menuItems.height()
	}, 300);
	menuOverlay.animate({
		opacity: '1'
	}, 300);
	menuItems.addClass('open');
}

function closeMenu() {
	menuItems.animate({
		bottom: '+=' + menuItems.height()
	}, 300);
	menuOverlay.animate({
		opacity: '0'
	}, 300);
	menuItems.removeClass('open');
}

//----------------------------------------------------------------

						 // LOGIC

//---------------------------------------------------------------/


//----------------------------------------------------------------

						 // AJAX CALLS

//---------------------------------------------------------------/



//----------------------------------------------------------------

						 // MAIN

//---------------------------------------------------------------/

/*******************************************
 * Main Function
*******************************************/


})(); // END OF NAV.JS